    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // Login button clicked in login window
        if(!(jPasswordField2.getPassword().length == 0) && !(jPasswordField1.getPassword().length == 0)){
            char[] newpassword = jPasswordField2.getPassword();
            char[] confpassword = jPasswordField1.getPassword();
            if(String.copyValueOf(newpassword).equals(String.copyValueOf(confpassword))){
                try {
                    Login login = new Login(this.module,this.doctor.getId());
                    if(login.changePasswd(newpassword)){
                        JOptionPane.showMessageDialog(rootPane, "The Password was Changed successfully!", "Change Successful!", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "The Password change failed", "Change Failure", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "The Passwords do not match!", "Password Mismatch", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            String opt = "";
            if((jPasswordField2.getPassword().length == 0) && !(jPasswordField1.getPassword().length == 0))
            opt = "New Password Field";
            else if(jPasswordField1.getPassword().length == 0 && !(jPasswordField2.getPassword().length == 0))
            opt = "Confirm Password Field";
            else if(jPasswordField2.getPassword().length == 0 && jPasswordField1.getPassword().length == 0)
            opt = "Both New Password Field and Confirm Password Field";
            JOptionPane.showMessageDialog(rootPane, opt+" cannot be empty.", opt+" Empty", JOptionPane.ERROR_MESSAGE);
            jPasswordField2.requestFocus();
        }
    }//GEN-LAST:event_jButton13ActionPerformed

