    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if(!model.isEmpty()){
            try {
                // done button clicked in lab test window
                this.labtest = new LabTest(this.patient.getId(),this.patientrecord.getId());
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(DoctorMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            for(int i = 0;i<model.getSize();i++){
                for (Test alltest : alltests) {
                    if(model.elementAt(i).toString().contains(alltest.getTitle())){
                        this.labtest.addTest(alltest);
                    }
                }
            }
            try {
                this.labtest.writeLabtest2DB();
                JOptionPane.showMessageDialog(rootPane, "Test submitted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(DoctorMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Cannot submit an empty test!", "Empty Test", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // View lab results button clicked
        this.resultmodel = new DefaultListModel();
        try {
            this.labtest = new LabTest();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(DoctorMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            this.labtest.getTestsfromDB(this.patientrecord.getId());
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(DoctorMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.testsresults = this.labtest.getTests();
        this.testsresults.stream().forEach((testt) -> {
            this.resultmodel.addElement(testt.getTitle());
        });
        jList2.setModel(this.resultmodel);
        
        jLabel26.setText(this.patient.getFirstname()+" "+this.patient.getLastname());
        jFrame4.setLocationRelativeTo(this);
        jFrame4.setVisible(true);
    }//GEN-LAST:event_jButton11ActionPerformed

    private void jList2ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList2ValueChanged
        // Lab results selection change in list model
        if(!this.testsresults.isEmpty()){
            this.testsresults.stream().forEach((testt) -> {
                if(!jList2.isSelectionEmpty()){
                    if(jList2.getSelectedValue().toString().contains(testt.getTitle())){
                        jTextArea4.setText(testt.getDescription());
                        jTextArea3.setText(testt.getResults());
                    }
                }
            });
        }
    }//GEN-LAST:event_jList2ValueChanged

