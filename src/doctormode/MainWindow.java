    private void checkDefaultAdmin() throws SQLException{
        String queryStr = "select * from Admin where username = ?"; 
        this.pstatement = this.connection.prepareStatement(queryStr);
        this.pstatement.setString(1, "admin");
        this.resultSet = this.pstatement.executeQuery();
        if(!this.resultSet.next()){
            queryStr = "insert into Admin (name, nationalID, username, password) values (?, ?, ?, ?)";
            this.pstatement = this.connection.prepareStatement(queryStr);
            this.pstatement.setString(1, "default admin");
            this.pstatement.setInt(2, 12345678);
            this.pstatement.setString(3, "admin");
            this.pstatement.setString(4, Login.getHash("admin"));
            this.pstatement.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Default Admin created:\n Username: admin \n Password: admin", "Default Admin", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
