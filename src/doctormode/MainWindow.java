    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Login button clicked in login window
        if (!jTextField1.getText().isEmpty() && !(jPasswordField1.getPassword().length == 0)) {
            String username = jTextField1.getText();
            char[] password = jPasswordField1.getPassword();
            try {
                Login login = new Login(this.module, username, password);
                if (login.isValid()) {
                    switch (this.module) {
                        case ("Doctor"):
                            new DoctorMode(this.module, login.getId());
                            break;
                        case ("Admin"):
                            new AdminDashbord().setVisible(true);
                            break;
                        case ("Receptionist"):
                            new ReceptionistMode(login.getId());
                            //new patient(login.getId()).setVisible(true);
                            break;
                        case ("LabTech"):
                            new JFlabTech().setVisible(true);
                            break;
                        case ("Cashier"):
                            new Receipt(login.getId()).setVisible(true);
                            break;
                    }
                    jFrame1.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "The provided credentials could not be authenticated", "Login Failure", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            String opt = "";
            if (jTextField1.getText().isEmpty() && !(jPasswordField1.getPassword().length == 0)) {
                opt = "Username Field";
            } else if (jPasswordField1.getPassword().length == 0 && !jTextField1.getText().isEmpty()) {
                opt = "Password Field";
            } else if (jTextField1.getText().isEmpty() && jPasswordField1.getPassword().length == 0) {
                opt = "Both Username Field and Password Field";
            }
            JOptionPane.showMessageDialog(rootPane, opt + " cannot be empty.", opt + " Empty", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

