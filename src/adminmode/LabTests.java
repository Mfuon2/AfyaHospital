    private void txtTitleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTitleKeyReleased
       String str;
        for (int i = 0; i < txtTitle.getText().trim().length(); i++) {
            str = (txtTitle.getText().trim().charAt(i) + "").trim();
            if (str.matches("[0-9]")) {
                JOptionPane.showMessageDialog(null, "Title cannot contain numbers!");
                txtTitle.setText(null);
            }
        }
    }//GEN-LAST:event_txtTitleKeyReleased

