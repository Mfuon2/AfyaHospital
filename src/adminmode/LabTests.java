    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {

            int docID;
            docID = Integer.parseInt(lblId.getText());
            String  title,charge1;
        
            title = txtTitle.getText();
            charge1 = txtCharge.getText();
            double charge = Double.parseDouble(charge1);

            try {
                pst = conn.prepareStatement("UPDATE Test SET title=?,charge=? WHERE id=?");
                pst.setString(1, title);
                pst.setDouble(2, charge);
                pst.setInt(3, docID);
                try {
                    pst.executeUpdate();
                //clear table contents
                while (tableTests.getRowCount() > 0) {
                    dm.removeRow(tableTests.getRowCount() - 1);
                }
                //updates the table contents
                Teststable();
                JOptionPane.showMessageDialog(null, "Update Successful!"); 
                } catch (Exception e) {
                  JOptionPane.showMessageDialog(null, "System do not accept Duplicates");  
                }
               

            } catch (SQLException ex) {
                Logger.getLogger(CreateDoctor.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Update Failed");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select a Record to Update!");
        }                                                                  
    }//GEN-LAST:event_btnUpdateActionPerformed

